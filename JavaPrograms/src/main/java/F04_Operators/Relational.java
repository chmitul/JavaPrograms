package F04_Operators;
//Relational operators are used to perform comparison between 2 variables.
//==, >, <, <=, >=, !=

public class Relational
{
	public static void main(String[] args)
	{
		int a = 10;
		int b = 20;
		int c = 10;

		System.out.println(a == b);
		System.out.println(a < b);
		System.out.println(a > b);
		System.out.println(a <= c);
		System.out.println(a >= c);
		System.out.println(a != b);

	}

}
