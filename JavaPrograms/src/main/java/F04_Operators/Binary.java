package F04_Operators;

//Binary operations are performed on 2 variables.
// Binary operators are  +,-,*,/,%

public class Binary
{
	public static void main(String[] args)
	{
		int a = 7;
		int b = 20;

		System.out.println(a + b);
		System.out.println(a - b);
		System.out.println(a * b);
		System.out.println(b / a);
		System.out.println(b % a);
		System.out.println("Hello " + " World"); // + can be used to concatenate the Strings.

	}

}
